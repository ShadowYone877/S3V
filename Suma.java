//Se crea la clase Suma

class Suma {

    //Se utiliza el método más importante(main)
    public static void main(String[] args) {
        /*Se declaran las variables con su tipo de dato 
        y se les asisgnan valores(a r no se le asigna ningun 
        valor porque es la variable que guardará el 
        resultado de nuestra suma)*/
        int a = 6, b = 9, r;
        //Se realiza la suma y r almacena el resultado
        r = a + b;
        //Se imprime el resultado en pantalla
        System.out.println(r);
    }
}
